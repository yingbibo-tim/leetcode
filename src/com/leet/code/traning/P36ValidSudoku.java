	//leetcode submit region begin(Prohibit modification and deletion)
	class Solution {
		public boolean isValidSudoku(char[][] board) {
			Map<Integer, Set<Character>> rowMap = new HashMap<>();
			Map<Integer, Set<Character>> columnMap = new HashMap<>();
			Map<Integer, Set<Character>> partMap = new HashMap<>();


			for (int i = 0; i < 9; i++) {
				for (int j = 0; j < 9; j++) {
					char c = board[i][j];
					if (c != '.') {
						{
							Set<Character> set = rowMap.get(i);
							if (set == null) {
								set = new HashSet<>();
							}
							if (set.contains(c)) {
								return Boolean.FALSE;
							}
							set.add(c);
							rowMap.put(i, set);
						}

						{
							Set<Character> set = columnMap.get(j);
							if(set == null ){
								set = new HashSet<>();
							}
							if(set.contains(c)){
								return Boolean.FALSE;
							}
							set.add(c);
							columnMap.put(j,set);
						}

						{
							int index = i/3 *3 + j/3;
							Set<Character> set = partMap.get(index);
							if(set == null) {
								set = new HashSet<>();
							}
							if(set.contains(c)){
								return Boolean.FALSE;
							}
							set.add(c);
							partMap.put(index,set);
							
						}





					}

				}
			}
			return Boolean.TRUE;
		}
	}
